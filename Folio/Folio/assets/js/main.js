// form validation and mailsending

var username = false
var useremail = false
var usernumber = false
var usersub = false
var usermsg = false
function namecheck() {
  var name = document.getElementById("name").value
  var letters = /^[A-Z a-z]+$/;
  if (name.match(letters)) {
    username = true
    document.getElementById("nameerror").innerText=""
  } else {
    username = false
    document.getElementById("nameerror").innerText="Enter a valid name"
  }
}
function mailcheck() {
  var mail = document.getElementById("email").value;
  var regx = /\b^[^ ][a-z.\-_0-9]+@[a-z0-9]+\.[a-z]{2,3}\b/
  if (mail.match(regx)) {
    useremail = true
    document.getElementById("emailerror").innerText=""
  } else {
    useremail = false
    document.getElementById("emailerror").innerText="Enter Email Id"
  }
}
function numcheck() {
  var num = document.getElementById("number").value;
  var digits = /\b[0-9]{10}\b/;
  if (num.match(digits)) {
    usernumber = true
    document.getElementById("numerror").innerText=""
  } else {
    usernumber = false
    document.getElementById("numerror").innerText="Enter valid  phone number"
  }
}
function subcheck() {
  var sub = document.getElementById("subject").value;
  if (sub == "") {
    usersub = false
    document.getElementById("suberror").innerText="Enter subject"
  } else {
    usersub = true
    document.getElementById("suberror").innerText=""
  }
}
function msgcheck() {
  var msg = document.getElementById("message").value;
  if (msg == "") {
    usermsg = false
    document.getElementById("msgerror").innerText="Enter a message"
  } else {
    usermsg = true
    document.getElementById("msgerror").innerText=""

  }

}
